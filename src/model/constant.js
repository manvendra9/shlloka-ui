export const validate = {
    password: /^.*(?=.{8,20})(?=.*\d)(?=.*[A-Z]+)(?=.*[a-z]+)(?=.*[~!@#$%^&\-*_=+`|()\][{}:;'"<>,.?\\/]+).*$/,
    // phone: /^[\d+()-\s]+$/,
    phone: /^[\d]+$/,
    money: /(^[1-9]\d*((\.[1-9]{1,2})|(\.0[1-9]))?$)|(^0((\.[1-9]{1,2})|(\.0[1-9]))?$)/,
    email: /^([A-Za-z0-9_\-.+])+@([A-Za-z0-9_\-.])+\.([A-Za-z]{2,})$/,
    decimals: (decimals = 2) => new RegExp(`^(\\d+|\\d+\\.\\d{0,${decimals}})$`),
    fullName: /^([a-zA-Z ]){2,30}$/
}